package lesson_07;

public class SwitchExample {
    public static void main(String[] args) {

        int x = 7;
        switch (x) {
            case 5:
                System.out.println("switch");
                break;
            case 7:
                System.out.println("go");
                break;
            default:
                System.out.println("no");

        }
    }
}
